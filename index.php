          <h2 class="section-title">Centre for HPC Upskilling & Knowledge-sharing (C-HUK)</h2>
          <p class="section-text">
            <strong>C-HUK</strong>, established by <strong>C-DAC Bengaluru</strong> under the <strong>Human Resource
            Development (HRD) initiative of the National Supercomputing Mission (NSM)</strong>, focuses on inclusive
            skill development in <strong>High Performance Computing (HPC)</strong> and allied emerging technologies.
          </p>
          <p class="section-text">
            Targeting <strong>SC, ST, Women</strong>, and <strong>EWS</strong> candidates, C-HUK provides expert
            training in <strong>HPC</strong>, <strong>AI</strong>, <strong>Embedded Systems</strong>, and <strong>Cyber
            Security</strong> to build a skilled and diverse tech talent pool.
          </p>
          <p class="section-text">
            Its programs include a comprehensive <strong>6-month Advanced Certificate Course in HPC</strong>,
            <strong>5-day Faculty Development Programmes (FDPs)</strong>, conducted in <strong>Bengaluru</strong> and
            the <strong>North Eastern region</strong> in collaboration with <strong>C-DAC’s Centre in North East
            (CINE)</strong>.
          </p>
          <p class="section-text">
            Through the <strong>Blended Learning Programme (BLP)</strong>, C-HUK delivers flexible, scalable content via
            the <strong>IEEE BLP Platform</strong> to learners across the globe.
          </p>
          <p class="section-text">
            C-HUK is instrumental in nurturing an inclusive, future-ready HPC ecosystem in alignment with India’s vision
            for digital empowerment and growth.
