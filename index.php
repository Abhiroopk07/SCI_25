          <h2 class="section-title">National Supercomputing Mission (NSM)</h2>
          <p class="section-text">
            The <strong>National Supercomputing Mission</strong> is a transformative initiative by the Government of
            India to democratize access to world-class computational power and infrastructure nationwide.
          </p>
          <p class="section-text">
            Jointly steered by the <strong>Ministry of Electronics and Information Technology (MeitY)</strong> and the
            <strong>Department of Science and Technology (DST)</strong>, and implemented by <strong>C-DAC</strong> and
            <strong>IISc</strong>, NSM drives cutting-edge R&D, capacity building, and innovation across academia,
            industry, and research.
          </p>
          <p class="section-text">
            By enabling equitable access to advanced supercomputing resources, NSM fuels technological excellence and
            supports strategic sectors, from weather forecasting to pharmaceutical design, and more.
          </p>
